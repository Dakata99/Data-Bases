USE SHIPS

SELECT DISTINCT c.COUNTRY
FROM CLASSES c
    LEFT OUTER JOIN SHIPS s ON s.CLASS = c.CLASS
    LEFT OUTER JOIN OUTCOMES o ON s.NAME = o.SHIP
WHERE s.LAUNCHED = 1942 AND RESULT = 'ok' AND NUMGUNS = 8